import { ResolverXML } from "../core/types/resolver";

export const GitResolver:ResolverXML<['pull','create']> = {
  pull:{
    name:'pull',
    query:{
      packet:{
        extension:{
          call:{
            git:{
              fetch:{
                domain:'{domain}',
                name:'{domain}'
              },
              deploy:{
                domain:'{domain}',
                name:'{domain}'
              }
            }
          }
        }
      }
    }
  },
  create:{
    name:'create',
    query:{
      packet:{
        extension:{
          call:{
            git:{
              create:{
                domain:'{domain}',
                name:'{domain}',
                "remote-url":'{remoteUrl}',
                "deployment-path":'{path}',
                "deployment-mode":'auto'
              }
            }
          }
        }
      }
    }
  }
}